    public static function emoji2text($string)
    {
        $emoji = array_search($string, self::$emojis);

        if ($emoji) {
            return $emoji;
        }
    }

    public static function text2emoji($string)
    {
        if (array_key_exists($string, self::$emojis)) {
            return self::$emojis[$string];
        }

        return $string;
    }
